#include <iostream>
using namespace std;

// 시험: student, courseroster 잘 보기

// ---------------------------
// 클래스 선언부 (붕어빵 틀)
// ---------------------------
class Person {
private:
    // ---------------------------
    // 멤버 변수 (state) = 객체 상태
    // ---------------------------
    string name;   // 이름
    int age;       // 나이

public:
    // ---------------------------
    // 생성자(Constructor)
    // 객체(instance)를 만들 때 자동으로 호출됨
    // ---------------------------
    Person(string n, int a) {       // 매개변수(Parameter): n, a
        name = n;                   // 상태 초기화
        age = a;                    // 상태 초기화
    }

    // ---------------------------
    // 멤버 함수(Method) = 객체 행동(behavior)
    // ---------------------------
    void introduce() {             // 행동: 자기 소개
        cout << "안녕! 나는 " << name << ", " << age << "살이야!" << endl;
    }

    void setAge(int a) {           // 행동: 나이 바꾸기
        age = a;                   // 상태(state) 변경
    }

    int getAge() {                 // 행동: 나이 확인
        return age;                // 상태(state) 읽기
    }

    // ---------------------------
    // 소멸자(Destructor) → 객체가 사라질 때 자동 호출
    // ---------------------------
    ~Person() {
        cout << name << " 객체가 사라졌어요!" << endl;
    }
};

// ---------------------------
// main() 함수 - 프로그램 시작점
// ---------------------------
int main() {
    // ---------------------------
    // 스택(Stack)에 인스턴스 생성
    // ---------------------------
    Person p1("철수", 10);        // 생성자 호출 → 상태 초기화
    p1.introduce();               // 행동(Method) 호출
    p1.setAge(11);                // 행동(Method) 호출로 상태 변경
    cout << "철수 나이: " << p1.getAge() << endl; // 상태 확인(Method)

    // ---------------------------
    // 힙(Heap)에 인스턴스 생성
    // ---------------------------
    Person* p2 = new Person("영희", 12); // 생성자 호출 → 상태 초기화
    p2->introduce();                      // 포인터로 행동(Method) 호출 → '->' 사용
    delete p2;                            // 힙 객체 삭제 → 소멸자 호출

    // ---------------------------
    // 프로그램 종료 시 스택 객체 자동 소멸
    // ---------------------------
    return 0;
}